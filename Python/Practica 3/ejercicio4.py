# Pedimos los números por teclado
number1 = int(input("Introduce el primer número: "))
number2 = int(input("Introduce el segundo número: "))
# Declaramos las variables de las operaciones
add = number1 + number2
subtract = number1 - number2
multiply = number1 * number2
division = number1 / number2

print(f"El resultado de la suma es {add}, de la resta es {subtract}, de la multiplicación es {multiply}, y de la división es {division}")
